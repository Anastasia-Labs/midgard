import { mkdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const sourcePath = path.resolve(__dirname, "../../midgard-sdk/dist/index.d.ts");
const targetPath = path.resolve(
  __dirname,
  "../src/generated/midgard-sdk-types.d.ts",
);

const header = `/**
 * Generated from ../midgard-sdk/dist/index.d.ts.
 * This keeps midgard-node's declaration build on a single local type graph
 * even when the linked SDK package carries its own dependency tree.
 * Do not edit this file directly; rerun the sync script instead.
 */

`;

/**
 * Copies the built SDK declaration file into midgard-node's generated types directory.
 */
const main = async () => {
  const source = await readFile(sourcePath, "utf8");
  await mkdir(path.dirname(targetPath), { recursive: true });
  await writeFile(targetPath, `${header}${source}`);
};

await main();
